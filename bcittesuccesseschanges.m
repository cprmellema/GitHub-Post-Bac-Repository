		'WHERE fMC.unit = fBC.unit AND egMC.fromunit = egBC.fromunit AND fMC.modelID = 44 AND fBC.modelID = 44 AND fMC.analyses_id = fBC.analyses_id '...
		'AND fconstBC.modelID = 31 AND fconstMC.modelID = 31 '...
		'AND EXISTS (SELECT * FROM `bci_units` bci WHERE bci.`ID` = et.`1DBCrecording` AND bci.unit = fMC.unit) '...
		'AND fMC.unit = "' bci_unit{1} '" '...
		'AND et.`manualrecording` = "' mcfile '"']));
		if strcmp(TE.Data, 'No Data')
			skipthisone = 1;
			continue;
		end		
		otherte(j,1:2) = cell2mat(TE.Data(:,5:6));
	end
	if skipthisone == 1
		continue;
	end

%'AND egMC.fromunit = "' unit '" '...
	diffcotuned = diff(cotunedte, 1,2);
	diffother = diff(otherte, 1,2);
	%pause 

	dcotuned = [dcotuned(:); diffcotuned(:)];
	dother = [dother(:); diffother(:)];
end

