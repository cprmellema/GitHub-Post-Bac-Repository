[fnBCIin,xinBCIin] = ksdensity(dother, 'width', 3, 'npoints', 600);
plot(xiBCIin,fBCIin, xinBCIin,fnBCIin);
xlabel('MCTE - BCTE')
ylabel('Density')
xlim([-50 50])
legend('Cotuned with BC-unit', 'Randomly selected unit')
